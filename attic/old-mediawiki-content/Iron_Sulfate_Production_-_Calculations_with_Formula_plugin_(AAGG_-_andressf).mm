  <body>
    <p>
      The method of production of&#160;&#160;iron sulfate (III)&#160;&#160;following the chemical reaction described here (immersion of iron oxid in sulfuric acid) has only an illustration aim: to show the possibility of the uses of calculations with the Freeplane Formula plugin. It is not the common method used in industrial production.
    </p>
    <p>
      
    </p>
    <p>
      The accompanying comments to the calculations below have also the same exclusive aim.
    </p>
    <p>
      The problem and the method of calculation and the requirements&#160;&#160;of more complex calculations are explained further in this text. But before, we synthesize briefly how the Formula plugin operates in this simple model:
    </p>
    <p>
      
    </p>
    <table border="0" style="border-right-width: 0; border-left-width: 0; border-top-width: 0; width: 80%; border-style: solid; border-bottom-width: 0">
      <tr>
        <td style="border-right-width: 1; border-left-width: 1; border-top-width: 1; width: 100%; border-style: solid; border-bottom-width: 1" valign="top">
          <p bgcolor="#ffff00" style="margin-top: 1; margin-right: 1; margin-bottom: 1; margin-left: 1; font-weight: normal; background-color: #ffff00; font-style: normal">
            <b>The independent variable in the calculations is the 'Required Output (Kgr)' of Iron Sulfate (III). Therefore, given the amount of iron sulfate required as final output, the Formula plugin calculates automatically the necessary inputs of iron oxid and sulfuric acid and the resulatant byproduct (water). </b>
          </p>
          <p bgcolor="#ffff00" style="margin-top: 1; margin-right: 1; margin-bottom: 1; margin-left: 1; font-weight: normal; background-color: #ffff00; font-style: normal">
            <b>So, for example, we want to know how much of inputs is needed and what the byproduct is to rpoduce 1 Kgr of iron sulfate. </b>
          </p>
          <p bgcolor="#ffff00" style="margin-top: 1; margin-right: 1; margin-bottom: 1; margin-left: 1; font-weight: normal; background-color: #ffff00; font-style: normal">
            <b>We replace thus the quantity 'a' by 1 in the respective attribute. Formula plugin then automatically calculates that to produce 1 Kgr of iron sulfate we need 0.40 Kgr of iron oxid and 0.74 of sulfuric acid, the resulting byproduct of the process being 0,13 Kgr of water. </b>
          </p>
          <p bgcolor="#ffff00" style="margin-top: 1; margin-right: 1; margin-bottom: 1; margin-left: 1; font-weight: normal; background-color: #ffff00; font-style: normal">
            <b>By changing successively the value of the attribute 'Required Output (Kgr)' (the independent variable 'a') Formula plugin will automatically recalculate how much of inputs is needed and how much of byproduct is produced.</b>
          </p>
        </td>
      </tr>
    </table>
    <p>
      
    </p>
    <p>
      
    </p>
    <p>
      <i><font color="#0066cc">Andr&#233;s GG </font></i>
    </p>
    <p>
      <i><font color="#0066cc">(andressf), </font></i><font color="#0066cc">September/November 2010</font>
    </p>
    <p>
      -------------
    </p>
    <p>
      <b>THE PROBLEM </b>
    </p>
    <p>
      !) calculate the raw material (iron oxid and sulfuric acid) that is needed to obtain a kgr of iron sulfate (III).
    </p>
    <p>
      2) how much is the byproduct?
    </p>
    <p>
      
    </p>
    <p>
      <b>THE CALCULATION </b>
    </p>
    <p>
      There are many similar ways to calculate the asked quantities. One of the possibilities is so as next specified:
    </p>
    <p>
      
    </p>
    <p>
      <b>1) raw materia</b>l
    </p>
    <p>
      1.1) the quantity (Kgr) of iron oxid needed is calculated with the formula:
    </p>
    <p>
      (159.69/ 399.88)x(a)
    </p>
    <p>
      or
    </p>
    <p>
      0.40 a
    </p>
    <p>
      This is thus the formula that must be introduced in the value field of the attribute &quot;Required Input (Kgr)&quot; in the attributes table of Iron Oxid. The numerical view of that field would be the answer to the first part of question 1 of the problem.
    </p>
    <p>
      
    </p>
    <p>
      1.2) the quantity (Kgr) of sulfuric acid&#160;&#160;needed is calculated, similarly, with the formula:
    </p>
    <p>
      (294.24/399.88)x(a)
    </p>
    <p>
      or
    </p>
    <p>
      0.74 a
    </p>
    <p>
      This is thus the formula that must be introduced in the value field of the attribute &quot;Required Input (Kgr)&quot; in the attributes table of Sulfuric Acid. The numerical view of that field would be the answer to the second part of the&#160;&#160;question 1 of the problem.
    </p>
    <p>
      
    </p>
    <p>
      <b>2)&#160;byproduct</b>
    </p>
    <p>
      The quantity (Kgr) of water resulting as byproduct of the fabrication of iron sulfate (III) would be:
    </p>
    <p>
      (54.04/399.88)x(a)
    </p>
    <p>
      or
    </p>
    <p>
      0.13 a
    </p>
    <p>
      which is the formula to be introduced in the value field of the attribute &quot;Resultant Byproduct (Kgr)&quot; in the attributes table of Water. The numerical view of that field would be the answer to the second&#160;question of the problem.
    </p>
    <p>
      
    </p>
    <p>
      <b>MORE COMPLEX CALCULATIONS</b>
    </p>
    <p>
      1) A similar problems, according to the degree of complexity of the calculation procedure would be to calculate the resulting weight of iron sulfate (III) when the weights of the two inputs are given and these weights are in the rate
    </p>
    <p>
      (iron oxid/sulfuric acid) = (159.69/294.24) = 0.54 (Kgr of iron oxid by Kgr of sulfuric acid)
    </p>
    <p>
      Similarly, a weight of one of the inputs could be given and would be asked how much of the other would be needed and the weights of the resulting outputs [iron sulfate (III) and byproduct water].
    </p>
    <p>
      
    </p>
    <p>
      2) However, more demanding calculations would arise when the raw materials are not in the mentioned proportion (0.54). In such case, there would be a surplus of one of the inputs. We would have to calculate this surplus&#160;&#160;previously to the calculation of the inputs and outpus. The problem in this case would thus imply an &quot;if&quot;. If we state, for instance, that we want to take the amount of iron oxid as base of the production, then&#160;&#160;we would have to pose:
    </p>
    <p>
      if (weight of iron oxid) &lt; [0.54x(weight of sulfuric acid)] then calculate the&#160;&#160;sulfuric acid needed by the given weight of iron oxid; this would be:
    </p>
    <p>
      (1/0.54)x(weight of iron oxid)
    </p>
    <p>
      or
    </p>
    <p>
      (1.85)x(weight of iron oxid)
    </p>
    <p>
      The surplus of&#160;&#160;sulfurpic acid would be:
    </p>
    <p>
      [given quantity of sulfuric acid - (1.85)x(weight of iron oxid)]. We would not need this result further to calculate the &quot;true problem&quot;: how much iron sulfate and water are produced when the given weights of raw material are combined.
    </p>
